typedef long long ll;

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> w(n);
    rep(i, n){
        cin >> w[i];
    }
    vector<int> s(n);
    s[0] = w[0];
    for(int i = 1; i < n; i++){
        s[i] = s[i - 1] + w[i];
    }
    int ans = abs(s[0] - (s[n - 1] - s[0]));
    for(int i = 1; i < n - 1; i++){
        ans = min(ans, abs(s[i] - (s[n - 1] - s[i])));
    }
    cout << ans << endl;
    return 0;
}
