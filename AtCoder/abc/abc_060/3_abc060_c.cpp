#define MOD (1000000007)
typedef long long ll;

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, t;
    cin >> n >> t;
    vector<int> a(n);
    rep(i, n){
        cin >> a[i];
    }

    int b = a[0];
    ll ans = t;

    for(int i = 1; i < n; i++){
        int c = a[i];
        if(a[i] - b >= t){
            ans += t;
        }else{
            ans += c - b;
        }
        b = c;
    }

    cout << ans << endl;
    return 0;
}
