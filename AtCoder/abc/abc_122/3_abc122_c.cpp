    ios::sync_with_stdio(false);
    int n, q;
    cin >> n >> q;
    string s;
    cin >> s;

    vector<int> sum(n + 1);
    sum[0] = 0;
    rep1(i, n){
        if(s.substr(i - 1, 2) == "AC"){
            sum[i] = sum[i - 1] + 1;
        }else{
            sum[i] = sum[i - 1];
        }
    }

    rep(i, q){
        int l, r;
        cin >> l >> r;
        l--; r--;

        cout << sum[r] - sum[l] << endl;
    }
