int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n, k;
    cin >> n >> k;

    string s;
    cin >> s;

    int ans = 0;
    char tmp = s[0];
    int nrev = 0;

    for(int i = 1; i < n; i++){
        if(tmp == s[i]) ans++;
        else nrev++;

        tmp = s[i];
    }

    if(nrev >= 2 * k){
        ans += 2 * k;
    }else{
        ans += (nrev / 2) * 2;
        ans += nrev % 2;
    }

    cout << ans << endl;

    return 0;
}
