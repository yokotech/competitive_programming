typedef long long ll;

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);
    int n;
    cin >> n;
    vector<int> a(n);

    ll x0 = 0;
    rep(i, n){
        cin >> a[i];
        if(i % 2 == 0) x0 += a[i];
        else x0 -= a[i];
    }
    vector<int> x(n);
    x[0] = x0 / 2;
    for(int i = 1; i < n; i++){
        x[i] = a[i - 1] - x[i - 1];
    }

    rep(i, n){
        cout << x[i] * 2 << " ";
    }
    cout << endl;

    return 0;
}
