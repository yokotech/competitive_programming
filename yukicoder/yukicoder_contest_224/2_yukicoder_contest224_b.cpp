typedef long long ll;

bool is_prime(ll num)
{
    if (num < 2) return false;
    else if (num == 2) return true;
    else if (num % 2 == 0) return false; // 偶数はあらかじめ除く

    double sqrtNum = sqrt(num);
    for (ll i = 3; i <= sqrtNum; i += 2)
    {
        if (num % i == 0)
        {
            // 素数ではない
            return false;
        }
    }

    // 素数である
    return true;
}


int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);

    ll n;
    cin >> n;
    ll k = sqrt(n);

    map<int, int> m;

    for(int i = 2; i <= k; i++){
        if(n % i == 0) m[i] = 0;

        while(n % i == 0){
            n /= i;
            m[i]++;
        }
    }

    ll ans;
    if(n != 1){
        ans = n + 1;
    }else{
        ans = 1;
    }

    for(int i = 2; i <= k; i++){
        if(m[i] == 0) continue;

        ans *= (pow(i, m[i] + 1) - 1) / (i - 1);
    }

    cout << ans << endl;

    return 0;
}
